class IUCNFetcher:
    BASE_V3 = "https://apiv3.iucnredlist.org/api/v3"
    BASE_V4 = "https://api.iucnredlist.org/api/v4"

    def __init__(self):
        self.token      = IUCN_API_TOKEN
        self.session    = requests.Session()
        self.cache      = {}
        self.cache_file = os.path.join(CACHE_DIR, "iucn_status_v5.json")
        self._load_cache()

    def _load_cache(self):
        os.makedirs(CACHE_DIR, exist_ok=True)
        if os.path.exists(self.cache_file):
            try:
                with open(self.cache_file, "r") as f:
                    self.cache = json.load(f)
            except Exception:
                self.cache = {}

    def _save_cache(self):
        try:
            with open(self.cache_file, "w") as f:
                json.dump(self.cache, f, indent=2)
        except Exception:
            pass

    def _query_v4(self, species_name: str) -> Optional[str]:
        time.sleep(IUCN_QUERY_DELAY)
        headers = {"Authorization": f"Bearer {self.token}",
                   "Accept": "application/json"}
        url = (f"{self.BASE_V4}/taxa/scientific_name?"
               f"name={species_name.replace(' ', '%20')}")
        r = self.session.get(url, headers=headers, timeout=15)
        if r.status_code == 404: return None
        if r.status_code != 200:
            raise RuntimeError(f"IUCN v4 HTTP {r.status_code}")
        data = r.json()
        if isinstance(data, dict):
            taxon = data.get("taxon") or data
            cat = taxon.get("red_list_category") or {}
            if isinstance(cat, dict):
                code = cat.get("code") or cat.get("category")
                if code: return code
            if "category" in taxon: return taxon["category"]
            assessments = data.get("assessments") or []
            if isinstance(assessments, list) and assessments:
                latest = assessments[0]
                code = (latest.get("red_list_category", {}) or {}).get("code")
                if code: return code
        return None

    def _query_v3(self, species_name: str) -> Optional[str]:
        time.sleep(IUCN_QUERY_DELAY)
        url = (f"{self.BASE_V3}/species/"
               f"{species_name.replace(' ', '%20')}?token={self.token}")
        r = self.session.get(url, timeout=15)
        if r.status_code != 200:
            raise RuntimeError(f"IUCN v3 HTTP {r.status_code}")
        results = r.json().get("result", [])
        if results:
            return results[0].get("category", "") or None
        return None

    def get_status(self, species_name: str) -> Optional[str]:
        key = species_name.lower().strip()
        if key in self.cache: return self.cache[key]
        if not self.token: return None

        result = None
        if USE_IUCN_V4:
            result = _retry(self._query_v4, species_name,
                            what=f"IUCNv4[{species_name}]")
        if not result:
            result = _retry(self._query_v3, species_name,
                            what=f"IUCNv3[{species_name}]")

        self.cache[key] = result
        return result

    def fetch_for_species_list(self, species_names):
        if not self.token:
            logger.info("IUCN API token not set — skipping")
            return
        to_fetch = [n for n in species_names
                    if n.lower().strip() not in self.cache]
        if not to_fetch: return
        logger.info(f"IUCN: querying {len(to_fetch)} species "
                    f"({'v4→v3' if USE_IUCN_V4 else 'v3 only'})")
        found = 0
        for name in tqdm(to_fetch, desc="IUCN API", leave=False):
            if self.get_status(name): found += 1
        self._save_cache()
        logger.info(f"IUCN: {found}/{len(to_fetch)} found")

