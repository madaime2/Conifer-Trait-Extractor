# Use the GraphQL endpoint first. The old REST endpoint started returning
# "not_found" for everything after WFO's June 2025 migration. Keep REST
# as a fallback in case GraphQL goes down or changes

class TaxonomyValidator:
    WFO_GRAPHQL = "https://list.worldfloraonline.org/gql.php"
    WFO_REST    = "https://list.worldfloraonline.org/matching_rest.php"

    GQL_QUERY = """
    query Match($s: String!) {
      taxonNameMatch(inputString: $s) {
        match {
          wfo_id
          full_name_string_plain
          taxonomicStatus
          family
          accepted {
            wfo_id
            full_name_string_plain
            family
          }
        }
        match_score
      }
    }
    """

    def __init__(self):
        self.session    = requests.Session()
        self.session.headers.update({"User-Agent": USER_AGENT,
                                     "Accept": "application/json"})
        self.cache      = {}
        self.cache_file = os.path.join(CACHE_DIR, "wfo_taxonomy_v5_9.json")
        self._load_cache()
        # Stop hammering GraphQL after 5 consecutive failures
        self._gql_consecutive_failures = 0
        self._gql_disabled = False

    def _load_cache(self):
        os.makedirs(CACHE_DIR, exist_ok=True)
        if os.path.exists(self.cache_file):
            try:
                with open(self.cache_file, "r") as f:
                    raw = json.load(f)
                for k, v in raw.items():
                    obj = _safe_dataclass_load(TaxonMatch, v)
                    if obj is not None:
                        self.cache[k] = obj
            except Exception as e:
                logger.warning(f"WFO cache load failed: {e}")
                self.cache = {}

    def _save_cache(self):
        try:
            with open(self.cache_file, "w") as f:
                json.dump({k: asdict(v) for k, v in self.cache.items()},
                          f, indent=2)
        except Exception as e:
            logger.debug(f"WFO cache save failed: {e}")

    def _query_wfo_graphql(self, species_name: str) -> Optional[dict]:
        time.sleep(WFO_QUERY_DELAY)
        payload = {"query": self.GQL_QUERY,
                   "variables": {"s": species_name}}
        resp = self.session.post(
            self.WFO_GRAPHQL,
            json=payload,
            headers={"Content-Type": "application/json"},
            timeout=20
        )
        if resp.status_code != 200:
            raise RuntimeError(f"WFO GraphQL HTTP {resp.status_code}")
        try:
            data = resp.json()
        except Exception:
            raise RuntimeError("WFO GraphQL returned non-JSON")
        errors = data.get("errors")
        if errors:
            logger.debug(f"WFO GraphQL errors for '{species_name}': {errors[:1]}")
            return None
        gdata = data.get("data") or {}
        match_block = gdata.get("taxonNameMatch")
        if not match_block:
            return None
        if isinstance(match_block, list):
            match_block = match_block[0] if match_block else None
        if not match_block:
            return None
        return match_block

    def _query_wfo_rest(self, species_name: str) -> Optional[dict]:
        time.sleep(WFO_QUERY_DELAY)
        resp = self.session.get(
            self.WFO_REST,
            params={"input_string": species_name},
            timeout=15
        )
        if resp.status_code != 200:
            raise RuntimeError(f"WFO REST HTTP {resp.status_code}")
        ct = resp.headers.get("content-type", "")
        if "json" not in ct:
            return None
        return resp.json()

    def _extract_match_record(self, blob) -> Optional[dict]:
        # Normalize GraphQL and REST shapes (list, dict, {"matches": [...]}) into
        # a flat record with scientific_name, taxonomic_status, family, accepted_*
        if blob is None: return None

        if isinstance(blob, dict) and ("match" in blob or "match_score" in blob):
            inner = blob.get("match")
            score = blob.get("match_score") or blob.get("score") or 0
            if isinstance(inner, dict):
                return self._normalise_match_dict(inner, score)
            return None

        if isinstance(blob, list):
            return self._extract_match_record(blob[0]) if blob else None

        if isinstance(blob, dict):
            if "matches" in blob and isinstance(blob["matches"], list):
                if blob["matches"]:
                    return self._extract_match_record(blob["matches"][0])
                return None
            return self._normalise_match_dict(blob, blob.get("score") or 0)

        return None

    def _normalise_match_dict(self, m: dict, score) -> dict:
        sci = (m.get("full_name_string_plain")
               or m.get("scientificName")
               or m.get("scientific_name")
               or "")
        status = (m.get("taxonomicStatus")
                  or m.get("taxonomic_status")
                  or "").lower()
        family = m.get("family") or m.get("acceptedFamily") or ""
        accepted_name = sci
        accepted_family = family
        acc = (m.get("accepted")
               or m.get("currentPreferredUsage")
               or m.get("acceptedTaxon"))
        if isinstance(acc, dict):
            inner_name = acc.get("name") if isinstance(acc.get("name"), dict) else acc
            accepted_name = (inner_name.get("full_name_string_plain")
                             or inner_name.get("scientificName")
                             or sci)
            accepted_family = (acc.get("family")
                               or inner_name.get("family")
                               or family)
        try:
            score_f = float(score)
        except (TypeError, ValueError):
            score_f = 0.0
        return {
            "scientific_name":  sci,
            "taxonomic_status": status,
            "family":           family,
            "accepted_name":    accepted_name,
            "accepted_family":  accepted_family,
            "match_score":      score_f,
        }

    def match_name(self, species_name: str) -> TaxonMatch:
        key = species_name.lower().strip()
        if key in self.cache: return self.cache[key]
        result = TaxonMatch(query_name=species_name)

        rec = None
        if not self._gql_disabled:
            try:
                blob = _retry(self._query_wfo_graphql, species_name,
                              what=f"WFO-GQL[{species_name}]")
                rec = self._extract_match_record(blob)
                if rec is None:
                    self._gql_consecutive_failures += 1
                else:
                    self._gql_consecutive_failures = 0
                if self._gql_consecutive_failures >= 5:
                    logger.warning(
                        "WFO GraphQL failing repeatedly — disabling for this run")
                    self._gql_disabled = True
            except Exception as e:
                logger.debug(f"WFO GraphQL exception: {e}")
                self._gql_consecutive_failures += 1

        if rec is None:
            blob = _retry(self._query_wfo_rest, species_name,
                          what=f"WFO-REST[{species_name}]")
            rec = self._extract_match_record(blob)

        if rec is not None and rec["scientific_name"]:
            result.matched_name = rec["scientific_name"]
            result.match_score  = rec["match_score"]
            status = rec["taxonomic_status"]
            if "accepted" in status:
                result.status          = "accepted"
                result.accepted_name   = rec["accepted_name"]
                result.accepted_family = rec["accepted_family"]
            elif "synonym" in status:
                result.status          = "synonym"
                result.accepted_name   = rec["accepted_name"]
                result.accepted_family = rec["accepted_family"]
            else:
                result.status          = "unresolved"
                result.accepted_name   = rec["scientific_name"] or species_name
                result.accepted_family = rec["family"]
        else:
            result.status        = "not_found"
            result.accepted_name = species_name

        self.cache[key] = result
        return result

    def validate_species_list(self, species_names):
        to_query = [n for n in species_names
                    if n.lower().strip() not in self.cache]
        if to_query:
            logger.info(f"WFO: {len(to_query)} to query "
                        f"({len(species_names)-len(to_query)} cached)")
            for name in tqdm(to_query, desc="WFO taxonomy", leave=False):
                self.match_name(name)
            self._save_cache()
        return {
            name: self.cache.get(
                name.lower().strip(),
                TaxonMatch(query_name=name, accepted_name=name, status="not_found")
            )
            for name in species_names
        }

