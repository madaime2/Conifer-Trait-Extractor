class KewCValuesIntegrator:
    BASE_URL = "https://cvalues.science.kew.org/search"

    EXPECTED_KEW_COLUMN_HINTS = ("species", "1c", "chromosome", "ploidy")

    def __init__(self):
        self.session = requests.Session()
        self.session.headers.update({
            "User-Agent": USER_AGENT,
            "Accept": "text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8"
        })
        self.cache      = {}
        self.cache_file = os.path.join(CACHE_DIR, "kew_cvalues_v5.json")
        self._load_cache()

    def _load_cache(self):
        os.makedirs(CACHE_DIR, exist_ok=True)
        if os.path.exists(self.cache_file):
            try:
                with open(self.cache_file, "r") as f:
                    raw = json.load(f)
                for k, v in raw.items():
                    obj = _safe_dataclass_load(KewRecord, v)
                    if obj is not None:
                        self.cache[k] = obj
            except Exception as e:
                logger.warning(f"Kew cache load failed: {e}")
                self.cache = {}

    def _save_cache(self):
        try:
            with open(self.cache_file, "w") as f:
                json.dump({k: asdict(v) for k, v in self.cache.items()},
                          f, indent=2)
        except Exception:
            pass

    def _select_data_table(self, html_text: str) -> Optional[pd.DataFrame]:
        # Pick the table whose columns best match the expected Kew signature
        try:
            tables = pd.read_html(html_text)
        except ValueError:
            return None
        if not tables: return None

        best = None
        best_score = 0
        for df in tables:
            if df.empty: continue
            cols_norm = " ".join(str(c).lower() for c in df.columns)
            score = sum(1 for hint in self.EXPECTED_KEW_COLUMN_HINTS
                        if hint in cols_norm)
            if score > best_score:
                best_score = score
                best = df
        if best is not None and best_score >= 2:
            return best
        return None

    def _query_species_once(self, genus: str, epithet: str) -> KewRecord:
        result = KewRecord(genus=genus, species=epithet)
        time.sleep(KEW_QUERY_DELAY)
        resp = self.session.post(self.BASE_URL, data={
            "search_type": "Gymnosperm", "prime_estimates": "1",
            "show_fields[]": ["genus","species","chromosome","ploidy","reference"],
            "cvalue": "onec", "genus": genus, "species": epithet
        }, timeout=KEW_TIMEOUT)
        resp.raise_for_status()

        df = self._select_data_table(resp.text)
        if df is None: return result

        df.columns = (df.columns.str.strip().str.lower()
                      .str.replace(" ", "_")
                      .str.replace(r"[^\w]", "_", regex=True))
        df = df.replace("-", pd.NA).replace("", pd.NA)
        result.found = True

        cvc = next((c for c in df.columns if "1c" in c and "pg" in c), None)
        if cvc:
            vals = pd.to_numeric(df[cvc], errors="coerce").dropna()
            if len(vals):
                result.genome_size_1c_pg = round(vals.mean(), 2)
                result.genome_size_min   = round(vals.min(),  2)
                result.genome_size_max   = round(vals.max(),  2)
                result.genome_size_n     = len(vals)

        chrc = next((c for c in df.columns if "chromosome" in c), None)
        if chrc:
            cv = df[chrc].dropna().astype(str).str.strip()
            cv = cv[cv != ""]
            if len(cv):
                result.chromosome_values = ",".join(sorted(cv.unique()))
                nums = [int(m.group(1)) for v in cv
                        for m in [re.search(r"(\d+)", v)] if m]
                if nums:
                    result.chromosome_2n = Counter(nums).most_common(1)[0][0]

        plc = next((c for c in df.columns if "ploidy" in c), None)
        if plc:
            pv = df[plc].dropna().astype(str).str.strip()
            pv = pv[pv != ""]
            if len(pv):
                result.ploidy_values = ",".join(sorted(pv.unique()))
                mc = Counter(pv).most_common(1)[0][0]; pl = mc.lower()
                if   "2x" in pl or "diploid"    in pl or mc == "2":
                    result.ploidy_level = "diploid"
                elif "4x" in pl or "tetraploid" in pl:
                    result.ploidy_level = "tetraploid"
                elif "6x" in pl or "hexaploid"  in pl:
                    result.ploidy_level = "hexaploid"
                elif "3x" in pl or "triploid"   in pl:
                    result.ploidy_level = "triploid"
                else:
                    result.ploidy_level = mc
        return result

    def _query_species(self, genus: str, epithet: str) -> KewRecord:
        result = _retry(self._query_species_once, genus, epithet,
                        what=f"Kew[{genus} {epithet}]")
        if result is None:
            return KewRecord(genus=genus, species=epithet)
        return result

    def fetch_for_species_list(self, species_list: List[str]):
        to_fetch = [sp for sp in species_list
                    if sp.lower().strip() not in self.cache]
        if not to_fetch:
            logger.info("All species in Kew cache"); return
        logger.info(f"Kew: {len(to_fetch)} to query "
                    f"({len(species_list)-len(to_fetch)} cached)")
        found = cval = chrom = ploid = 0
        for sp in tqdm(to_fetch, desc="Kew C-values", leave=False):
            parts = sp.strip().split()
            # Drop × hybrid marker before query
            parts = [p for p in parts if p != "×"]
            if len(parts) < 2: continue
            rec = self._query_species(parts[0], parts[1])
            self.cache[sp.lower().strip()] = rec
            if rec.found:
                found += 1
                cval  += bool(rec.genome_size_1c_pg)
                chrom += bool(rec.chromosome_2n)
                ploid += bool(rec.ploidy_level)
        self._save_cache()
        logger.info(f"Kew: {found}/{len(to_fetch)} found, "
                    f"C={cval} chr={chrom} pl={ploid}")

    def get_record(self, name: str) -> Optional[KewRecord]:
        return self.cache.get(name.lower().strip())

    def get_summary_df(self) -> pd.DataFrame:
        return pd.DataFrame([
            {"species":           f"{r.genus} {r.species}",
             "genome_size_1c_pg": r.genome_size_1c_pg,
             "genome_size_n":     r.genome_size_n,
             "genome_size_min":   r.genome_size_min,
             "genome_size_max":   r.genome_size_max,
             "chromosome_2n":     r.chromosome_2n,
             "chromosome_values": r.chromosome_values,
             "ploidy_level":      r.ploidy_level,
             "ploidy_values":     r.ploidy_values}
            for r in self.cache.values() if r.found
        ])

