def merge_range_traits(rec: TraitRecord) -> TraitRecord:
    pairs = [
        ("leaf_length_min_mm",       "leaf_length_max_mm"),
        ("leaf_width_min_mm",        "leaf_width_max_mm"),
        ("seed_length_min_mm",       "seed_length_max_mm"),
        ("seed_cone_length_min_cm",  "seed_cone_length_max_cm"),
        ("seed_cone_width_min_cm",   "seed_cone_width_max_cm"),
        ("pollen_cone_length_min_mm","pollen_cone_length_max_mm"),
        ("elevation_min_m",          "elevation_max_m"),
    ]
    for min_t, max_t in pairs:
        vmin = getattr(rec, min_t); vmax = getattr(rec, max_t)
        if vmin is None and vmax is None: continue
        if vmin is None: setattr(rec, min_t, vmax)
        if vmax is None: setattr(rec, max_t, vmin)
        vmin = getattr(rec, min_t); vmax = getattr(rec, max_t)
        if vmin is not None and vmax is not None and vmin > vmax:
            setattr(rec, min_t, vmax); setattr(rec, max_t, vmin)
    return rec

def validate_trait_record(rec: TraitRecord) -> List[str]:
    flags = []; d = rec.to_dict()
    for trait, (lo, hi) in TRAIT_BOUNDS.items():
        val = d.get(trait)
        if val is not None and val != "":
            try:
                v = float(val)
                if v < lo or v > hi:
                    flags.append(f"{trait}={v} outside [{lo},{hi}]")
            except (ValueError, TypeError):
                pass
    pairs = [
        ("leaf_length_min_mm",       "leaf_length_max_mm"),
        ("leaf_width_min_mm",        "leaf_width_max_mm"),
        ("pollen_cone_length_min_mm","pollen_cone_length_max_mm"),
        ("seed_cone_length_min_cm",  "seed_cone_length_max_cm"),
        ("seed_cone_width_min_cm",   "seed_cone_width_max_cm"),
        ("seed_length_min_mm",       "seed_length_max_mm"),
        ("elevation_min_m",          "elevation_max_m"),
        ("cotyledon_number_min",     "cotyledon_number_max"),
    ]
    for min_t, max_t in pairs:
        mn, mx = d.get(min_t), d.get(max_t)
        if mn is not None and mx is not None:
            try:
                if float(mn) > float(mx):
                    flags.append(f"{min_t}={mn} > {max_t}={mx} (swapped)")
                    setattr(rec, min_t,
                        _safe_float(mx) if isinstance(mn, float) else _safe_int(mx))
                    setattr(rec, max_t,
                        _safe_float(mn) if isinstance(mx, float) else _safe_int(mn))
            except (ValueError, TypeError):
                pass
    return flags

def apply_elevation_regex_guard(rec: TraitRecord, source_text: str) -> bool:
    # Null out elevations that only appear in mm-context (e.g., "5500 mm" rainfall)
    if rec.elevation_min_m is None and rec.elevation_max_m is None:
        return False
    text_lower = source_text.lower()
    for val in [rec.elevation_min_m, rec.elevation_max_m]:
        if val is None: continue
        try:
            val_int = str(int(round(float(val))))
        except (TypeError, ValueError):
            continue
        occurrences = [m.start()
                       for m in re.finditer(r'\b' + val_int + r'\b', text_lower)]
        if not occurrences: continue
        all_mm = True
        for pos in occurrences:
            ctx = text_lower[max(0, pos-60): pos+40]
            after = text_lower[pos + len(val_int): pos + len(val_int) + 5]
            if re.match(r'\s*m(?!m)\b', after):
                all_mm = False; break
            if 'elev' in ctx or 'altitud' in ctx:
                all_mm = False; break
        if all_mm:
            logger.debug(
                f"Elevation guard: nulling {val} m — only found in mm context")
            rec.elevation_min_m = None
            rec.elevation_max_m = None
            return True
    return False

