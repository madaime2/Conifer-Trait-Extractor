class TraitMatrixPipeline:
    def __init__(self):
        self.fetcher    = ParallelFetcher()
        self.discoverer = ParallelDiscoverer(self.fetcher)
        self.llm        = LLMExtractor()
        self.kew        = KewCValuesIntegrator() if FETCH_KEW_CVALUES else None
        self.iucn       = IUCNFetcher() if IUCN_API_TOKEN else None
        self.taxonomy   = TaxonomyValidator() if VALIDATE_TAXONOMY else None
        self.augmenter  = DeterministicAugmenter(kew=self.kew, iucn=self.iucn)

    def run(self):
        global token_tracker
        token_tracker = TokenTracker()
        os.makedirs(OUTPUT_DIR, exist_ok=True)
        start_time = time.time()

        print("\n" + "="*72)
        print("  CONIFERS.ORG TRAIT MATRIX EXTRACTOR")
        print("="*72)
        print(f"  Model:          {MODEL}")
        print(f"  Prompt version: {PROMPT_VERSION}")
        print(f"  Pricing date:   {TokenTracker.PRICING_DATE}")
        print(f"  IUCN:           {'v4→v3 fallback' if USE_IUCN_V4 else 'v3 only'} "
              f"(token={'set' if IUCN_API_TOKEN else 'missing'})")
        print(f"  Output:         {OUTPUT_DIR}")
        print("="*72 + "\n")

        # Discovery
        all_pages = self.discoverer.discover_all()
        self.discoverer.write_unmatched_log(
            os.path.join(OUTPUT_DIR, "unmatched_urls.log"))
        self.discoverer.write_monotypic_log(
            os.path.join(OUTPUT_DIR, "monotypic_detections.tsv"))

        pages = self._filter_pages(all_pages)
        logger.info(f"Filtered down to {len(pages)} target species")

        # Taxonomy
        taxon_map = (self.taxonomy.validate_species_list(
                         [p.species_name for p in pages])
                     if self.taxonomy else {})

        # Fetch + parse
        html_map = self.fetcher.get_many(
            [p.url for p in pages], desc="Fetching species pages")

        items = []
        for p in tqdm(pages, desc="Parsing HTML", leave=False):
            html = html_map.get(p.url)
            if html:
                text, tt = html_to_text_and_tables(html)
                items.append((p, text, tt))

        sp_names = [p.species_name for p in pages]
        if self.kew:  self.kew.fetch_for_species_list(sp_names)
        if self.iucn: self.iucn.fetch_for_species_list(sp_names)

        # Pass 1 + Pass 2 extraction
        print(f"\n{'='*72}\n  PASS 1: LLM extraction — {len(items)} species\n{'='*72}\n")
        records_a          = []
        raw_jsons          = []
        errors             = []
        all_flags          = []
        all_hallucinations = []
        elev_requery_count = 0
        elev_guard_count   = 0
        p1 = {"n": 0, "lock": threading.Lock()}

        def do_pass1(item):
            sp, text, tt = item
            llm_json, err = self.llm.extract_pass1(
                sp.species_name, sp.family, sp.genus, text, tt)
            return sp, text, llm_json, err

        with ThreadPoolExecutor(max_workers=MAX_CONCURRENT_LLM) as pool:
            futs = {pool.submit(do_pass1, it): it for it in items}
            for fut in tqdm(as_completed(futs), total=len(futs),
                            desc="Pass 1", leave=False):
                sp, source_text, llm_json, err = fut.result()
                if err:
                    errors.append((sp.species_name, err))
                    if not llm_json: llm_json = {}

                if llm_json:
                    llm_json, nulled = verify_evidence(llm_json, source_text)
                    if nulled:
                        all_hallucinations.append({
                            "species":      sp.species_name,
                            "nulled_count": len(nulled),
                            "details":      "; ".join(nulled[:5])
                        })

                tm  = taxon_map.get(sp.species_name)
                rec = self.llm.json_to_record(sp, llm_json, tm)
                rec = merge_range_traits(rec)

                elev_calls_this_species = 0

                # Pass 2 fires when elevation is missing or min == max (often a
                # single-bounded "up to X" extraction with no real lower bound)
                needs_pass2 = (
                    rec.elevation_min_m is None
                    or rec.elevation_max_m is None
                    or (rec.elevation_min_m is not None
                        and rec.elevation_max_m is not None
                        and rec.elevation_min_m == rec.elevation_max_m)
                )

                if needs_pass2 and elev_calls_this_species < MAX_ELEV_LLM_CALLS_PER_SPECIES:
                    elev2 = self.llm.requery_elevation(sp.species_name, source_text)
                    elev_calls_this_species += 1
                    new_min = elev2["elevation_min_m"]
                    new_max = elev2["elevation_max_m"]
                    if new_min is not None or new_max is not None:
                        if new_min is not None: rec.elevation_min_m = new_min
                        if new_max is not None: rec.elevation_max_m = new_max
                        if new_min is not None or new_max is not None:
                            elev_requery_count += 1

                # mm-context guard, then optional re-query
                if apply_elevation_regex_guard(rec, source_text):
                    elev_guard_count += 1
                    if elev_calls_this_species < MAX_ELEV_LLM_CALLS_PER_SPECIES:
                        elev2 = self.llm.requery_elevation(sp.species_name, source_text)
                        elev_calls_this_species += 1
                        if elev2["elevation_min_m"] is not None:
                            rec.elevation_min_m = elev2["elevation_min_m"]
                        if elev2["elevation_max_m"] is not None:
                            rec.elevation_max_m = elev2["elevation_max_m"]

                rec = merge_range_traits(rec)

                flags = validate_trait_record(rec)
                if flags:
                    all_flags.append({
                        "species": sp.species_name,
                        "flags":   "; ".join(flags)
                    })

                records_a.append(rec)
                raw_jsons.append((sp, llm_json))

                with p1["lock"]:
                    p1["n"] += 1; n = p1["n"]

                if n % CHECKPOINT_EVERY == 0 or n == len(items):
                    avg     = (sum(r.count_filled() for r in records_a)
                               / max(len(records_a), 1))
                    elapsed = time.time() - start_time
                    rate    = n / max(elapsed, 1) * 60
                    eta     = (len(items) - n) / max(rate/60, 0.001)
                    print(f"\n--- [{n}/{len(items)}] {token_tracker.summary()}")
                    print(f"  Avg: {avg:.1f}/41 | Err: {len(errors)} | "
                          f"Flags: {len(all_flags)} | "
                          f"Halluc: {len(all_hallucinations)} | "
                          f"ElevRequery: {elev_requery_count} | "
                          f"ElevGuard: {elev_guard_count} | "
                          f"ETA: {eta/60:.1f}min")
                    if n % CHECKPOINT_EVERY == 0:
                        pd.DataFrame([r.to_dict() for r in records_a]).to_csv(
                            os.path.join(OUTPUT_DIR, "checkpoint_pass1.csv"),
                            index=False)

        print(f"\n{'='*72}\n  PASS 1+2 DONE — {token_tracker.summary()}\n{'='*72}")
        print(f"  Elevation Pass-2 requeries: {elev_requery_count}")
        print(f"  Elevation guard nullifications: {elev_guard_count}\n")

        # Augmentation
        records_b = []; all_sources = []; all_overrides = []
        for rec_a in tqdm(records_a, desc="Augmenting", leave=False):
            rec_b, src, overrides = self.augmenter.augment(rec_a)
            records_b.append(rec_b)
            all_sources.append(src)
            all_overrides.extend(overrides)

        # Build DataFrames
        df_a = pd.DataFrame([r.to_dict() for r in records_a])
        df_b = pd.DataFrame([r.to_dict() for r in records_b])
        df_c = df_b.copy()
        skip = {"species","accepted_name","genus","family","source_url","taxonomic_status"}
        src_rows = []
        for i, sd in enumerate(all_sources):
            row = {}
            for t in TRAIT_COLUMNS:
                if t in skip: continue
                if t in sd:
                    row[f"{t}_source"] = sd[t]
                else:
                    va = df_a.iloc[i].get(t) if i < len(df_a) else None
                    has_val = (va is not None and va != ""
                               and not (isinstance(va, float) and pd.isna(va)))
                    row[f"{t}_source"] = SourceType.TEXT if has_val else SourceType.EMPTY
            src_rows.append(row)
        df_c = pd.concat([df_c, pd.DataFrame(src_rows)], axis=1)

        # Save
        csv_a = os.path.join(OUTPUT_DIR, "dataset_A_text_evidenced.csv")
        csv_b = os.path.join(OUTPUT_DIR, "dataset_B_augmented.csv")
        csv_c = os.path.join(OUTPUT_DIR, "dataset_C_sourced.csv")
        df_a.to_csv(csv_a, index=False)
        df_b.to_csv(csv_b, index=False)
        df_c.to_csv(csv_c, index=False)

        with open(os.path.join(OUTPUT_DIR, "dataset_A_with_evidence.jsonl"), "w") as f:
            for sp, lj in raw_jsons:
                f.write(json.dumps({
                    "species": sp.species_name, "genus": sp.genus,
                    "family":  sp.family, "url": sp.url, "traits": lj},
                    ensure_ascii=False) + "\n")

        if self.kew:
            self.kew.get_summary_df().to_csv(
                os.path.join(OUTPUT_DIR, "kew_cvalues_summary.csv"), index=False)
        if taxon_map:
            pd.DataFrame([asdict(m) for m in taxon_map.values()]).to_csv(
                os.path.join(OUTPUT_DIR, "taxonomy_report.csv"), index=False)
        if all_flags:
            pd.DataFrame(all_flags).to_csv(
                os.path.join(OUTPUT_DIR, "validation_flags.csv"), index=False)
        if all_hallucinations:
            pd.DataFrame(all_hallucinations).to_csv(
                os.path.join(OUTPUT_DIR, "hallucination_report.csv"), index=False)
        if all_overrides:
            pd.DataFrame(all_overrides).to_csv(
                os.path.join(OUTPUT_DIR, "genus_overrides.csv"), index=False)

        diag = self._diagnostics(df_a, df_b, records_a, all_sources,
                                  all_flags, all_hallucinations, taxon_map,
                                  errors, elev_requery_count, elev_guard_count,
                                  all_overrides)
        with open(os.path.join(OUTPUT_DIR, "diagnostics.json"), "w") as f:
            json.dump(diag, f, indent=2)

        # Summary
        elapsed = time.time() - start_time
        print("\n" + "="*72 + "\n  EXTRACTION COMPLETE\n" + "="*72)
        print(f"  Time:    {elapsed:.1f}s ({elapsed/60:.1f} min) | "
              f"Species: {len(records_a)}")
        print(f"  LLM errors: {len(errors)} | Flags: {len(all_flags)} | "
              f"Hallucinations: {len(all_hallucinations)}")
