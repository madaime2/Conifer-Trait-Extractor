    def _filter_pages(self, pages: List[SpeciesPage]) -> List[SpeciesPage]:
        allowed = set(CONIFER_FAMILIES)
        if INCLUDE_CYCADS:   allowed |= CYCAD_FAMILIES
        if INCLUDE_GINKGO:   allowed |= GINKGO_FAMILIES
        if INCLUDE_GNETALES: allowed |= GNETALES_FAMILIES
        before = len(pages)
        pages  = [p for p in pages if p.family in allowed]
        if before - len(pages):
            logger.info(f"Filtered out {before-len(pages)} non-target species")
        if SCRAPE_MODE == "family":
            pages = [p for p in pages if p.family == TARGET_FAMILY]
        elif SCRAPE_MODE == "genus":
            pages = [p for p in pages if p.genus == TARGET_GENUS]
        if SPECIES_LIMIT:
            pages = pages[:SPECIES_LIMIT]
        return pages

    def _diagnostics(self, df_a, df_b, records_a, all_sources,
                     flags, hallucinations, taxon_map, errors,
                     elev_requery_count=0, elev_guard_count=0,
                     overrides=None):
        tc = [c for c in TRAIT_COLUMNS
              if c not in ("species","accepted_name","genus","family",
                           "source_url","taxonomic_status")]
        def comp(df):
            total = len(df) * len(tc); filled = 0
            for c in tc:
                if c in df.columns:
                    filled += df[c].notna().sum()
                    if df[c].dtype == object:
                        filled -= (df[c] == "").sum()
            return filled, total
        af, at = comp(df_a); bf, bt = comp(df_b)
        sc = Counter()
        for sd in all_sources:
            for t, s in sd.items(): sc[s] += 1
        return {
            "prompt_version":         PROMPT_VERSION,
            "evidence_logic_version": EVIDENCE_LOGIC_VERSION,
            "n_species": len(records_a),
            "dataset_a": f"{af}/{at} ({100*af/max(at,1):.1f}%)",
            "dataset_b": f"{bf}/{bt} ({100*bf/max(bt,1):.1f}%)",
            "source_counts":             dict(sc),
            "model":                     MODEL,
            "iucn_api":                  "v4→v3" if USE_IUCN_V4 else "v3",
            "llm_errors":                len(errors),
            "validation_flags":          len(flags),
            "hallucinations_caught":     len(hallucinations),
            "elevation_pass2_requeries": elev_requery_count,
            "elevation_guard_nulls":     elev_guard_count,
            "genus_overrides_of_text":   len(overrides) if overrides else 0,
            "taxonomy_synonyms": (
                sum(1 for m in taxon_map.values() if m.status == "synonym")
                if taxon_map else 0
            ),
            "taxonomy_accepted": (
                sum(1 for m in taxon_map.values() if m.status == "accepted")
                if taxon_map else 0
            ),
            "taxonomy_not_found": (
                sum(1 for m in taxon_map.values() if m.status == "not_found")
                if taxon_map else 0
            ),
            "monotypic_genus_species": sum(
                1 for r in records_a
                if r.source_url and re.search(
                    rf"/{re.escape(r.genus)}\.php$", r.source_url)
            ),
            "tokens": {
                "prompt":     token_tracker.total_prompt_tokens,
                "completion": token_tracker.total_completion_tokens,
                "reasoning":  token_tracker.total_reasoning_tokens,
                "cost_usd":   round(token_tracker.estimated_cost(), 4),
                "calls":      token_tracker.total_calls,
            }
        }

    def _print_completeness(self, df, label):
        tc = [c for c in TRAIT_COLUMNS
              if c not in ("species","accepted_name","genus","family",
                           "source_url","taxonomic_status")]
        total = len(df) * len(tc); filled = 0; tf = {}
        for c in tc:
            if c in df.columns:
                v = df[c].notna()
                if df[c].dtype == object: v = v & (df[c] != "")
                filled += v.sum(); tf[c] = v.sum()
        st = sorted(tf.items(), key=lambda x: x[1], reverse=True)
        print(f"\n  Dataset {label}:")
        print(f"    Completeness: {filled}/{total} ({100*filled/max(total,1):.1f}%)")
        print(f"    Best:  {', '.join(f'{t}({n})' for t,n in st[:5])}")
        print(f"    Worst: {', '.join(f'{t}({n})' for t,n in st[-5:])}")

    def _print_source_breakdown(self, all_sources):
        sc = Counter()
        for sd in all_sources:
            for t, s in sd.items(): sc[s] += 1
        print(f"\n  Sources:")
        for s, c in sc.most_common():
            if s: print(f"    {s}: {c}")

