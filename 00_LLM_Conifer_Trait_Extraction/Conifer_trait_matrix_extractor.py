def _is_useful_link(href: str, base_url: str) -> bool:
    # Keep only family directories, family indices, and the gymnosperms index
    if not href: return False
    if href.startswith(("#", "mailto:", "javascript:")): return False
    full = urljoin(base_url, href.strip())
    if "conifers.org" not in full: return False
    if ".php" not in full: return False
    path = urlparse(full).path
    if not path or not path.endswith(".php"): return False

    parts = path.strip("/").split("/")
    if len(parts) == 2:
        if parts[0] == "zz" and parts[1].startswith("gymnosperms"): return True
        if parts[0] in ALL_FAMILY_CODES: return True
    return False

def extract_links(html, base_url):
    soup = BeautifulSoup(html, "html.parser")
    out = []
    for a in soup.find_all("a", href=True):
        href = a["href"].strip()
        if _is_useful_link(href, base_url):
            out.append(urljoin(base_url, href))
    return out

def html_to_text_and_tables(html):
    soup = BeautifulSoup(html, "html.parser")
    for tag in soup(["script", "style", "nav", "header", "footer"]):
        tag.decompose()
    table_parts = []
    for i, tbl in enumerate(soup.find_all("table"), 1):
        table_parts.append(f"[TABLE {i}]")
        for tr in tbl.find_all("tr"):
            cells = [td.get_text(" ", strip=True)
                     for td in tr.find_all(["th", "td"])]
            if cells:
                table_parts.append(" | ".join(cells))
    text = re.sub(r"\s+", " ", soup.get_text(" ", strip=True)).strip()
    return text, "\n".join(table_parts)[:MAX_TABLE_CHARS]


def detect_monotypic_species_page(html: str, genus: str
                                   ) -> Optional[Tuple[str, str]]:
    # A /<code>/<Genus>.php URL can be either:
    #   - a species page for a monotypic genus (Austrocedrus, Sequoia, Fitzroya...)
    #   - or just a list of species in a multi-species genus (Pinus, Picea, Juniperus)
    # Tell them apart by looking for a Description section, a repeated binomial
    # ("Genus epithet" mentioned multiple times), and at most one sibling species link.
    if not html: return None
    soup = BeautifulSoup(html, "html.parser")

    for tag in soup(["script", "style", "nav", "header", "footer"]):
        tag.decompose()
    text = soup.get_text(" ", strip=True)

    has_description = bool(re.search(r"\bDescription\b", text)) and bool(
        re.search(r"\b(Distribution|Ecology|Leaves|Cones|Bark|Trees?)\b", text))
    if not has_description:
        return None

    sibling_species_links = 0
    for a in soup.find_all("a", href=True):
        href = a["href"]
        if re.search(rf"(?:^|/){re.escape(genus)}_[a-z][a-z0-9_-]*\.php$",
                     href):
            sibling_species_links += 1
    if sibling_species_links >= 2:
        return None

    head_text = text[:3000]
    pat = re.compile(rf"\b{re.escape(genus)}\s+([a-z][a-z\-]{{2,29}})\b")
    found_epithets = pat.findall(head_text)
    if not found_epithets:
        return None

    # Grammatical/taxonomic words that follow a genus name but aren't epithets
    BLOCKLIST = {
        "is", "was", "are", "were", "and", "or", "the", "a", "an",
        "as", "in", "of", "to", "from", "with", "by", "this", "that",
        "these", "those", "has", "have", "had", "be", "been", "being",
        "species", "genus", "trees", "shrubs", "occurs", "grows",
        "produces", "shows", "name", "names", "type", "differs",
        "described", "occurring", "represents", "appears", "section",
        "subgenus", "sect", "subsect", "florin", "linnaeus",
        "spp", "varieties", "var", "subsp", "ssp", "but", "however",
        "such", "all", "for", "into", "now", "only", "one", "other",
        "some", "than", "two", "very", "which", "while", "also",
        "new", "old", "found", "known", "may", "can", "will", "would",
    }
    valid = [e for e in found_epithets if e not in BLOCKLIST]
    if not valid:
        return None
    counter = Counter(valid)
    epithet, count = counter.most_common(1)[0]
    if count < 2:
        return None

    return (epithet, f"{genus} {epithet}")


DISTRIBUTION_HEADINGS = [
    "Distribution and Ecology", "Distribution & Ecology",
    "Distribution and ecology", "Distribution",
    "Ecology", "Ecology and Distribution", "Habitat and Distribution",
]
NEXT_SECTION_HEADINGS = [
    "Big Tree", "Big tree", "Remarkable Specimens", "Remarkable specimens",
    "Ethnobotany", "Observations", "Remarks", "Citations", "References",
    "See also", "See Also", "Conservation", "Dendrochronology", "Cultivation",
    "Genetic information", "Discovery", "History", "Phenology", "Notes",
    "Synonymy", "Taxonomy", "Etymology", "Acknowledgments",
]

def _extract_distribution_section(text: str) -> str:
    # Return text from "Distribution and Ecology" up to the next known section,
    # falling back to a middle-third chunk if no heading is found
    for heading in DISTRIBUTION_HEADINGS:
        idx = text.find(heading)
        if idx == -1: continue
        section = text[idx:]
        cut = len(section)
        for stop in NEXT_SECTION_HEADINGS:
            stop_idx = section.find(stop)
            if 100 < stop_idx < cut:
                cut = stop_idx
        # Soft signal: a title-case phrase at sentence end may also be a heading
        m = re.search(
            r"(?<=[.?!])\s+([A-Z][a-z]+(?:\s+[A-Z][a-z]+){0,3})\s+[A-Z]",
            section[200:]
        )
        if m:
            soft_cut = 200 + m.start()
            if soft_cut < cut and soft_cut > 200:
                cut = soft_cut
        return section[:cut][:MAX_DISTRIB_CHARS]
    mid = len(text) // 3
    return text[mid: mid + MAX_DISTRIB_CHARS]


class ParallelDiscoverer:
    def __init__(self, fetcher):
        self.fetcher = fetcher
        self.visited = set()
        self.species = {}
        self.unmatched_php = []
        self._lock   = threading.Lock()

    def _classify_url(self, url, html: Optional[str] = None
                      ) -> Optional[SpeciesPage]:
        # Genus-only URLs need HTML to decide whether they're a monotypic
        # species page. Without HTML we return None and re-check on the next pass
        path = urlparse(url).path

        m = SPECIES_URL_RE.match(path)
        if m:
            code, genus, epithet = m.groups()
            return SpeciesPage(url=url, family=family_from_code(code),
                               genus=genus, epithet=epithet,
                               is_monotypic_inferred=False)

        gm = GENUS_URL_RE.match(path)
        if gm:
            code, genus = gm.groups()
            if genus in FAMILY_NAME_PATHS:
                return None
            if html is None:
                return None
            inferred = detect_monotypic_species_page(html, genus)
            if inferred:
                epithet, _ = inferred
                return SpeciesPage(url=url, family=family_from_code(code),
                                   genus=genus, epithet=epithet,
                                   is_monotypic_inferred=True)
            return None

        # Log anything under a family directory that didn't match either pattern
        parts = path.strip("/").split("/")
        if (len(parts) == 2 and parts[0] in ALL_FAMILY_CODES
                and parts[1].endswith(".php")
                and parts[1] not in {"index.php"}
                and not parts[1].startswith(("pinaceae", "podocarpaceae",
                                             "cupressaceae", "araucariaceae",
                                             "taxaceae", "sciadopityaceae",
                                             "zamiaceae", "cycadaceae",
                                             "stangeriaceae", "ginkgoaceae",
                                             "ephedraceae", "welwitschiaceae",
                                             "gnetaceae"))):
            with self._lock:
                if url not in [u for u, _ in self.unmatched_php]:
                    self.unmatched_php.append((url, "regex_no_match"))
        return None

    def _process_page(self, url, html):
        new_links = []
        if not html: return new_links

        # Re-check the current page using its HTML. Genus-only URLs (like
        # /cu/Austrocedrus.php) couldn't be classified from the URL alone when
        # they were first discovered as links, so we catch them here
        sp_self = self._classify_url(url, html=html)
        if sp_self:
            with self._lock:
                key = sp_self.species_name.lower()
                if key not in {s.species_name.lower()
                               for s in self.species.values()}:
                    self.species[sp_self.url] = sp_self

        for link in extract_links(html, url):
            with self._lock:
                if link in self.visited: continue
                self.visited.add(link)
            sp = self._classify_url(link, html=None)
            if sp:
                with self._lock:
                    key = sp.species_name.lower()
                    if key not in {s.species_name.lower()
                                   for s in self.species.values()}:
                        self.species[sp.url] = sp
            else:
                new_links.append(link)
        return new_links

    def discover_all(self):
        # Stops when no new species AND no new links, or at the hard caps
        logger.info("Discovering species on conifers.org...")
        queue = [GYMNOSPERMS_INDEX]
        self.visited.add(GYMNOSPERMS_INDEX)

        for depth in range(MAX_CRAWL_DEPTH):
            if not queue:
                logger.info(f"  Crawl finished: queue empty at depth {depth}")
                break
            if len(self.visited) > MAX_CRAWL_PAGES:
                logger.warning(f"  MAX_CRAWL_PAGES ({MAX_CRAWL_PAGES}) hit; stopping")
                break

            n_species_before = len(self.species)
            logger.info(f"  Depth {depth+1}: queue={len(queue)} pages, "
                        f"species_so_far={n_species_before}, "
                        f"visited={len(self.visited)}")
            html_map = self.fetcher.get_many(queue, desc=f"Crawl d{depth+1}")
            next_q = []
            for url, html in html_map.items():
                if html:
                    next_q.extend(self._process_page(url, html))
            queue = list(set(next_q))

            n_species_after = len(self.species)
            new_species = n_species_after - n_species_before
            logger.info(f"    → +{new_species} new species "
                        f"(total {n_species_after}); +{len(queue)} new links")

            if new_species < MIN_NEW_SPECIES_TO_CONTINUE and not queue:
                logger.info("  Crawl complete: no new species or links")
                break

        logger.info(f"Discovery complete: {len(self.species)} species, "
                    f"{len(self.visited)} pages visited, "
                    f"{len(self.unmatched_php)} unmatched .php URLs logged")
        n_mono = sum(1 for s in self.species.values()
                     if s.is_monotypic_inferred)
        if n_mono:
            logger.info(f"  Monotypic-genus species detected: {n_mono}")
        return list(self.species.values())

    def write_unmatched_log(self, out_path):
        if not self.unmatched_php: return
        try:
            with open(out_path, "w") as f:
                f.write("url\treason\n")
                for url, reason in self.unmatched_php:
                    f.write(f"{url}\t{reason}\n")
        except Exception as e:
            logger.warning(f"Could not write unmatched log: {e}")

    def write_monotypic_log(self, out_path):
        mono = [s for s in self.species.values() if s.is_monotypic_inferred]
        if not mono: return
        try:
            with open(out_path, "w") as f:
                f.write("genus\tspecies\turl\tfamily\n")
                for s in mono:
                    f.write(f"{s.genus}\t{s.species_name}\t{s.url}\t{s.family}\n")
        except Exception as e:
            logger.warning(f"Could not write monotypic log: {e}")

