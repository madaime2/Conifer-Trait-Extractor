# Helpers for parsing messy values and cleaning up text
# ============================================================

_NULL_TOKENS = {"null", "none", "n/a", "na", "unknown",
                "not stated", "not mentioned", "not assessed",
                "not evaluated", "ne", ""}

def _safe_float(x):
    if x is None: return None
    try:    return round(float(x), 4)
    except: return None

def _safe_int(x):
    if x is None: return None
    try:    return int(round(float(x)))
    except: return None

def _safe_str(x):
    if x is None: return ""
    s = str(x).strip()
    if s.lower() in _NULL_TOKENS:
        return ""
    return s

def parse_llm_json(raw_text):
    # Tolerate markdown fences and trailing commas
    text = raw_text.strip()
    if "```" in text:
        parts = text.split("```")
        if len(parts) >= 3:
            inner = parts[1]
            if inner.lower().startswith("json"): inner = inner[4:]
            text = inner.strip()
    try: return json.loads(text)
    except json.JSONDecodeError: pass
    bs = text.find("{"); be = text.rfind("}")
    if bs != -1 and be > bs:
        try: return json.loads(text[bs:be+1])
        except json.JSONDecodeError: pass
        try:
            return json.loads(re.sub(r",\s*([}\]])", r"\1", text[bs:be+1]))
        except json.JSONDecodeError: pass
    return None

def _normalize_text(s: str) -> str:
    if not s: return ""
    s = s.lower()
    s = s.replace("\u2013", "-").replace("\u2014", "-")
    s = s.replace("\u2018", "'").replace("\u2019", "'")
    s = s.replace("\u201c", '"').replace("\u201d", '"')
    s = re.sub(r"\s+", " ", s)
    return s.strip()

# ============================================================
# Evidence verification
# ============================================================
# Check whether the LLM's evidence quote actually appears in the source text.
# Tries exact match first, then loosens to word checks and fuzzy overlap.
# Strips punctuation around words but keeps hyphens intact (so "scale-like"
# stays one word)

_PUNCT_STRIP = re.compile(r"^[^\w\-']+|[^\w\-']+$")

def _tokenize_for_match(s: str) -> set:
    out = set()
    for w in s.split():
        clean = _PUNCT_STRIP.sub("", w)
        if clean:
            out.add(clean)
    return out

def _tokenize_seq(s: str) -> list:
    return [_PUNCT_STRIP.sub("", w) for w in s.split()
            if _PUNCT_STRIP.sub("", w)]


def verify_evidence(llm_json: dict, source_text: str, threshold: float = 0.6):
    source_norm  = _normalize_text(source_text)
    source_words = _tokenize_for_match(source_norm)
    cleaned = {}; nulled = []

    for trait, obj in llm_json.items():
        if not isinstance(obj, dict):
            cleaned[trait] = obj; continue
        value    = obj.get("value")
        evidence = obj.get("evidence")
        if value is None:
            cleaned[trait] = obj; continue
        if not evidence or str(evidence).strip().lower() in _NULL_TOKENS:
            nulled.append(f"{trait}={value} (no evidence)")
            cleaned[trait] = {"value": None, "evidence": None}; continue

        ev_str = str(evidence).strip()

        # If the number itself appears in the source, accept it even when the
        # evidence phrase doesn't quite match. Catches cases where the LLM
        # paraphrases or uses a different dash/spacing than the source.
        # e.g., source has "4-6 cm" but the LLM quotes "4 to 6 cm"
        if trait in NUMERIC_TRAITS:
            try:
                val_str = str(int(round(float(value))))
                ev_norm_local = _normalize_text(ev_str)
                if val_str in source_norm and val_str in ev_norm_local:
                    cleaned[trait] = obj; continue
                if val_str in source_norm:
                    ev_words_local = _tokenize_for_match(ev_norm_local)
                    if ev_words_local:
                        ev_content = {w for w in ev_words_local if len(w) > 2}
                        if ev_content and (
                            len(ev_content & source_words) /
                            max(len(ev_content), 1) >= 0.4
                        ):
                            cleaned[trait] = obj; continue
            except (ValueError, TypeError):
                pass

        # chromosome_2n: accept "2n = N" patterns with any whitespace/punct
        if trait == "chromosome_2n" and value is not None:
            try:
                n = int(round(float(value)))
                pattern = rf"2\s*n\s*[=:]\s*{n}\b"
                if re.search(pattern, source_norm):
                    cleaned[trait] = obj; continue
            except (ValueError, TypeError):
                pass

        if _evidence_in_text(ev_str, source_norm, source_words, threshold):
            cleaned[trait] = obj
        else:
            nulled.append(
                f"{trait}={value} (evidence not in text: '{ev_str[:60]}')")
            cleaned[trait] = {"value": None, "evidence": None}

    return cleaned, nulled


def _evidence_in_text(evidence, source_norm, source_words, threshold):
    ev_norm = _normalize_text(evidence)
    if ev_norm in source_norm: return True

    ev_seq = _tokenize_seq(ev_norm)
    ev_words = set(ev_seq)

    if len(ev_seq) <= 2:
        return all(w in source_words for w in ev_words if len(w) > 2)

    for i in range(len(ev_seq) - 2):
        trig = " ".join(ev_seq[i:i+3])
        if trig in source_norm: return True

    ev_content = {w for w in ev_words if len(w) > 2}
    if not ev_content: return True
    return len(ev_content & source_words) / len(ev_content) >= threshold

