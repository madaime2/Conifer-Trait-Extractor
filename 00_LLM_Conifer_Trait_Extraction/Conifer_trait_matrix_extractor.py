# Adds Kew, IUCN, family-level constants, and genus exceptions to records.
# Genus exceptions that override a text-evidenced value are logged

class DeterministicAugmenter:
    def __init__(self, kew=None, iucn=None):
        self.kew  = kew
        self.iucn = iucn

    def augment(self, rec_a: TraitRecord) -> Tuple[TraitRecord, dict, list]:
        rec_b   = TraitRecord(**rec_a.to_dict())
        sources = {}
        override_entries = []

        # Kew C-values
        if self.kew:
            kr = self.kew.get_record(rec_a.species)
            if (not kr or not kr.found) and rec_a.accepted_name != rec_a.species:
                kr = self.kew.get_record(rec_a.accepted_name)
            if kr and kr.found:
                if kr.genome_size_1c_pg:
                    rec_b.genome_size_1c_pg = kr.genome_size_1c_pg
                    sources["genome_size_1c_pg"] = SourceType.KEW
                if kr.chromosome_2n:
                    rec_b.chromosome_2n = kr.chromosome_2n
                    sources["chromosome_2n"] = SourceType.KEW
                if kr.ploidy_level:
                    rec_b.ploidy_level = kr.ploidy_level
                    sources["ploidy_level"] = SourceType.KEW

        # IUCN status
        if self.iucn and IUCN_API_TOKEN:
            status = self.iucn.get_status(rec_a.species)
            if not status and rec_a.accepted_name != rec_a.species:
                status = self.iucn.get_status(rec_a.accepted_name)
            if status:
                rec_b.iucn_status = status
                sources["iucn_status"] = SourceType.IUCN_API

        # Family constants (fill missing only)
        family = rec_a.family
        if family in FAMILY_LEVEL_TRAITS:
            for t, v in FAMILY_LEVEL_TRAITS[family].items():
                cur = getattr(rec_b, t, None)
                if cur is None or cur == "":
                    setattr(rec_b, t, v)
                    if t not in sources:
                        sources[t] = SourceType.FAMILY

        # Genus exceptions (overwrite, log overrides of text values)
        genus = rec_a.genus
        if genus in GENUS_EXCEPTIONS:
            for t, v in GENUS_EXCEPTIONS[genus].items():
                cur_a = getattr(rec_a, t, None)
                had_text_value = (cur_a is not None and cur_a != ""
                                  and not (isinstance(cur_a, float)
                                           and pd.isna(cur_a)))
                if had_text_value and cur_a != v:
                    override_entries.append({
                        "species":             rec_a.species,
                        "genus":               genus,
                        "trait":               t,
                        "text_evidence_value": cur_a,
                        "genus_rule_value":    v,
                    })
                setattr(rec_b, t, v)
                sources[t] = SourceType.GENUS

        # Reapply habitat guard after augmentation
        if rec_b.habitat_type and rec_b.habitat_type.lower().strip() in BAD_HABITATS:
            rec_b.habitat_type = ""

        return rec_b, sources, override_entries

