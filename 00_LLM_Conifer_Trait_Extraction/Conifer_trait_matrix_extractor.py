# Retry wrapper for external API calls
def _retry(fn, *args, retries: int = EXTERNAL_API_RETRIES,
           backoff: float = EXTERNAL_API_BACKOFF, what: str = "request",
           **kwargs):
    last_err = None
    for attempt in range(retries):
        try:
            return fn(*args, **kwargs)
        except Exception as e:
            last_err = e
            if attempt < retries - 1:
                time.sleep(backoff * (2 ** attempt))
    logger.debug(f"{what} failed after {retries} attempts: {last_err}")
    return None

