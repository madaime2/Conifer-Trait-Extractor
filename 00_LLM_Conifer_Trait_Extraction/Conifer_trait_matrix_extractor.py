class ParallelFetcher:
    def __init__(self):
        self.session = requests.Session()
        self.session.headers.update({"User-Agent": USER_AGENT})
        self._lock = threading.Lock()
        self._last_request_time = 0.0
        os.makedirs(CACHE_DIR, exist_ok=True)

    def _cache_path(self, url):
        return os.path.join(
            CACHE_DIR,
            f"page_{hashlib.md5(url.encode()).hexdigest()[:16]}.html"
        )

    def _rate_limit(self):
        with self._lock:
            elapsed = time.time() - self._last_request_time
            if elapsed < REQUEST_DELAY:
                time.sleep(REQUEST_DELAY - elapsed)
            self._last_request_time = time.time()

    def get(self, url):
        if USE_DISK_CACHE:
            cp = self._cache_path(url)
            if os.path.exists(cp):
                try:
                    with open(cp, "r", encoding="utf-8") as f:
                        return f.read()
                except Exception:
                    pass
        self._rate_limit()
        try:
            r = self.session.get(url, timeout=TIMEOUT)
            r.raise_for_status()
            if USE_DISK_CACHE:
                try:
                    with open(self._cache_path(url), "w", encoding="utf-8") as f:
                        f.write(r.text)
                except Exception:
                    pass
            return r.text
        except Exception as e:
            logger.warning(f"Fetch failed {url}: {e}")
            return None

    def get_many(self, urls, desc="Fetching"):
        results = {}
        with ThreadPoolExecutor(max_workers=MAX_CONCURRENT_FETCHES) as pool:
            futs = {pool.submit(self.get, u): u for u in urls}
            for fut in tqdm(as_completed(futs), total=len(urls),
                            desc=desc, leave=False):
                results[futs[fut]] = fut.result()
        return results

