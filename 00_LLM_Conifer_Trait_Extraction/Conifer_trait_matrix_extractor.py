class LLMExtractor:
    def __init__(self):
        self.client = None
        api_key = os.environ.get("OPENAI_API_KEY", "").strip()
        if api_key:
            from openai import OpenAI
            self.client = OpenAI(api_key=api_key)
            logger.info(f"OpenAI client initialized (model={MODEL})")
        os.makedirs(LLM_CACHE_DIR,  exist_ok=True)
        os.makedirs(ELEV_CACHE_DIR, exist_ok=True)

    def _call_llm(self, system: str, user: str,
                  max_tokens: int = 3500) -> Optional[str]:
        if not self.client: return None
        for attempt in range(LLM_MAX_RETRIES):
            try:
                ml = MODEL.lower()
                is_reasoning = any(x in ml for x in ("gpt-5", "o1", "o3", "o4"))
                if is_reasoning:
                    resp = self.client.chat.completions.create(
                        model=MODEL, max_completion_tokens=16000,
                        messages=[{"role": "system", "content": system},
                                  {"role": "user",   "content": user}])
                else:
                    resp = self.client.chat.completions.create(
                        model=MODEL, temperature=LLM_TEMPERATURE,
                        max_tokens=max_tokens,
                        messages=[{"role": "system", "content": system},
                                  {"role": "user",   "content": user}])
                content = resp.choices[0].message.content or ""
                finish  = resp.choices[0].finish_reason or "unknown"
                token_tracker.record(getattr(resp, 'usage', None),
                                     finish, content.strip() == "")
                if content.strip() == "":
                    if attempt < LLM_MAX_RETRIES - 1:
                        time.sleep(LLM_RETRY_DELAY * (2 ** attempt))
                        continue
                    return None
                return content.strip()
            except Exception as e:
                token_tracker.record_failure()
                logger.debug(f"LLM call failure (attempt {attempt}): {e}")
                time.sleep(LLM_RETRY_DELAY * (2 ** attempt))
        return None

    def _elevation_cache_is_stale(self, cached: dict) -> bool:
        # Sanity-check cached elevations: if max is <= 200 m but neither "elev"
        # nor "altitud" appears in the saved evidence, the LLM probably misread
        # a millimeter measurement or a geographic feature height as elevation.
        # Don't trust the cache here and run the LLM again
        elev_min = cached.get("elevation_min_m", {})
        elev_max = cached.get("elevation_max_m", {})
        min_val  = elev_min.get("value") if isinstance(elev_min, dict) else elev_min
        max_val  = elev_max.get("value") if isinstance(elev_max, dict) else elev_max
        if min_val is None or max_val is None:
            return True
        try:
            if float(max_val) <= 200:
                ev_min = (elev_min.get("evidence", "") or ""
                          if isinstance(elev_min, dict) else "")
                ev_max = (elev_max.get("evidence", "") or ""
                          if isinstance(elev_max, dict) else "")
                combined = (ev_min + " " + ev_max).lower()
                if "elev" not in combined and "altitud" not in combined:
                    return True
        except (TypeError, ValueError):
            pass
        return False

